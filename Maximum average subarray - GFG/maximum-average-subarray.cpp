class Solution{   
public:
    int findMaxAverage(int arr[], int n, int k) {
        // code here
        int sm=accumulate(arr, arr+k, 0);
        int max_sum=sm;
        int index=0;
        for(int i=1,j=k;j<n;i++, j++)
        {
            sm=sm-arr[i-1]+arr[j];
            if(sm>max_sum)
            index=i, sm= max_sum;
        }
        return index;
    }
};

